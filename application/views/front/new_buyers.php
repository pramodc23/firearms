    <div class="follow_btnn pull-right"><span class="btn_inner"><a href="#">Follow</a></span></div>
    <div class="article-body"><p>Please remember&nbsp;by&nbsp;placing a bid you are placing a legally binding offer to purchase the item at any price up&nbsp;to your Maximum Bid amount. If you are the winning bidder, you are legally obligated to complete the transaction. This helps us&nbsp;provide a high-quality environment for both buyers and sellers.&nbsp;</p>
<p>We recommend you do the following before you buy an item:</p>
<p>1. Review <a href="/hc/en-us/articles/226246808">How to Bid and Buy on GunBroker.com</a>.</p>
<p>2. Review and ensure&nbsp;you understand&nbsp;<a href="/hc/en-us/articles/222007967">Legal Issues when Buying and Selling Firearms</a>.</p>
<p>3. Check the <a href="/hc/en-us/articles/222173227">Seller's Feedback Rating&nbsp;</a>to see how others have interacted with this seller by clicking&nbsp;the letter&nbsp;grade next to the seller's user name on the item listing page.</p>
<p>4. <a href="/hc/en-us/articles/221710267-Contacting-the-Seller-">Contact the seller</a>&nbsp;to get information, and carefully review the terms and conditions posted in the item listing.&nbsp;Make sure you clearly understand the shipping terms and amounts (who pays?), payment terms (can you pay by personal check, credit card, etc.), and other details. If you have questions about any of the terms, please contact the seller by clicking&nbsp;Ask Seller a Question.</p>
<p>Please remember&nbsp;when you bid&nbsp;on an auction, once you make and confirm a bid, you cannot <a href="/hc/en-us/articles/221530847">retract your bid</a>&nbsp;except in exceptional circumstances.</p>
<p>In order to&nbsp;maintain a consistently good experience&nbsp;for&nbsp;our members, we&nbsp;reserve the right to&nbsp;suspend the accounts of persons who win an item but fail to complete the transaction.</p></div>
