   <div class="buy_cnt_head pull-left">How to Bid and Buy</div>
    <div class="follow_btnn pull-right"><span class="btn_inner"><a href="#">Follow</a></span></div>
    <div class="article-body"><p><strong>Buying overview:</strong></p>
<ol>
<li>You buy a firearm from a seller.</li>
<li>You&nbsp; pay the seller directly.</li>
<li>You send the Federal Firearms License (FFL) information to the seller.</li>
<li>The seller ships the firearm to the FFL.</li>
<li>The FFL performs a background check with the National Instant Criminal Background Check System (NICS) and transfers the item to you.</li>
</ol>
<p>If the item does not require an FFL, the seller ships the item directly to you.</p>
<p>&nbsp;<strong>If you do not have an FFL: </strong></p>
<p>&nbsp;See <a href="#">Finding an FFL Dealer in Your Area</a>.</p>
<p><strong>Finding an item:</strong></p>
<p>There are several ways to find an item you are interested in purchasing: You can browse by category, search by keyword, or use the Advanced Search Form to search for multiple criteria, such as items with no reserve price, items available for immediate checkout, items with a starting bid within a specified range, and more.</p>
<p>To learn more about how to search for items, see:</p>
<ul>
<li><a href="#">Search Tips</a></li>
<li><a href="#">Searching Items Using the Advanced Search Form&nbsp;</a></li>
</ul>
<p><strong>Before you buy:</strong></p>
<p>Before you place a bid or purchase an item at a fixed price, review the details of the item you wish to purchase, including the seller's <a href="#">Feedback Ratings</a>, the item description, payment and shipping terms. If you still have&nbsp;questions, click Ask Seller a Question on the item listing page. See&nbsp;<a href="#">Before You Buy</a>.</p>
<p><strong>Bidding on auction listings:</strong></p>
<p>To place a bid on an item, enter your maximum bid from the item listing page. <a href="#">AutoBid</a>&nbsp;automatically bids on your behalf the minimum it can to win the bid, up to your maximum bid.</p>
<p>After you place a bid, we show you the status of your bid:</p>
<ul>
<li>If you place a bid that does not meet the seller's <a href="#">Reserve Price</a>, we will tell you.</li>
<li>If you are outbid by another bidder using AutoBid, we&nbsp;tell you.</li>
</ul>
<p>If you are outbid for any reason, you are no longer obligated to buy the item. You are then free to bid again or pursue another item.</p>
<p>Please remember:&nbsp;By&nbsp;placing a bid you are placing a legally binding offer to purchase the item at any price up through your Maximum Bid amount. If the seller accepts your bid,&nbsp;you are legally obligated to complete the transaction.</p>
<p>For more information about bidding and auctions, see:</p>
<ul>
<li><a href="#">Bidding: How to Buy Items in an Auction</a></li>
<li><a href="#">Our Auctions</a></li>
</ul>
<p><strong>Checking the status of your auctions:</strong></p>
<p>Check the status of your bids by navigating <a href="#">Welcome&gt;My Buying</a>. We keep&nbsp;track of items&nbsp;you&nbsp;bid on, items you won, and items you did not win on the My Buying page. For more information on monitoring the status of your auctions, see <a href="#">Monitoring Bids and Checking Buy Status</a>.</p>
<p><strong>Purchasing items immediately without bidding:</strong></p>
<p>Some items&nbsp;can be purchased immediately without bidding.</p>
<p><strong>Winning&nbsp;an auction:</strong></p>
<p>If you win an auction, we send you an email at the end of the auction that contains the seller's user name and email address along with your winning bid amount.&nbsp;You can also view <em>Items I've Won</em> by navigating Welcome&gt;My Buying.&nbsp;</p>
<p>From the time the auction closes you have a maximum of five (5) days to contact the seller or start the checkout process. &nbsp;The seller must also contact you or complete the order within&nbsp;five days.&nbsp;</p>
<p>For more information on&nbsp;what to do if you win an auction, see:</p>
<ul>
<li><a href="#">Winning an Auction</a></li>
<li><a href="#">Paying for the Item You Purchased or Won</a></li>
<li><a href="#">Submitting Feedback</a></li>
</ul>
