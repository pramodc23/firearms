    <div class="article-body">
    <p>Please remember&nbsp;by&nbsp;listing an item you are&nbsp;legally committing to accept the high bid for the item. If you receive a bid above the minimum price you set,&nbsp;and a reserve price is not specified,&nbsp;you are legally obligated to complete the transaction. The only exception is if completing the sale&nbsp;violates federal, state or local law. This helps us&nbsp;provide a high-quality environment for both buyers and sellers.</p>
<p>&nbsp;<strong>Here&nbsp;are some&nbsp;helpful rules and suggestions&nbsp;for&nbsp;listing your item:</strong></p>
<ol>
<li>You may place any merchandise&nbsp;for&nbsp;sale or auction&nbsp;which you have the right to sell if&nbsp;it complies with the User Agreement and does not violate any other posted rules of this site. All content linked to the item description must comply with the firearms.com&nbsp;<a href="#">User Agreement</a>.</li>
<li>Please list your item in an appropriate category. See&nbsp;<a href="#">Selecting the Correct Category</a>.</li>
<li>Your description of the item must be truthful and accurate.&nbsp;You should clarify any additional terms or expenses associated with the item, such as who will pay shipping and insurance expenses.</li>
<li>You should state how you expect to be paid (for example, personal check, money order, credit cards).</li>
<li>Warranty terms should be clearly outlined.</li>
<li>If you are required to collect sales tax, you must include this information in the&nbsp;listing terms.</li>
<li>Auctions in which the winning bidder is given a choice between various items are not encouraged. If you are unable to provide the buyer with the item he/she chooses, you are considered&nbsp;to have&nbsp;defaulted on the transaction.</li>
<li>In order to&nbsp;maintain a consistently good experience&nbsp;for&nbsp;our members, we&nbsp;reserve the right to&nbsp;suspend the accounts of persons who&nbsp;fail to complete&nbsp;transactions (unless&nbsp;the sale&nbsp;violates federal, state or local law).</li>
<li>If&nbsp;you start&nbsp;a listing&nbsp;and decide you&nbsp;no longer wish to sell&nbsp;the item,&nbsp;you&nbsp;can end the auction early using&nbsp;<a href="#">Tools for Sellers</a>. An auction cannot be ended early once there are bids on the item.</li>
<li>It is important to stay&nbsp;in communication with&nbsp;bidders/buyers and promptly respond to all inquiries.</li>
<li>A&nbsp;seller may not bid on his or her own auction.</li>
<li>Reserve Price auctions&nbsp;are often unpopular with bidders.&nbsp;We suggest&nbsp;you set your Starting Bid to the least amount you will sell the item for, and let the bidding begin at that price.&nbsp;See <a href="#">Reserve Prices and Starting Bids</a>.</li>
</ol>
