      <div class="content">
        <p><strong>If you are outbid:</strong></p>
<p><b>Auto Bid</b> enters bids automatically on behalf of other bidders. If you place a bid, and you&nbsp;are notified&nbsp;you are outbid, it is typically because another user placed a Maximum Bid higher than your bid, and&nbsp;AutoBid&nbsp;raised their bid higher than your bid.</p>
<p><strong>If you are&nbsp;outbid by less than the bid increment:</strong></p>
<p>If you lost the item by less than the bid increment, another bidder entered an amount above the next bid increment&nbsp;after you entered your bid.</p>
<p>Bid increments are determined by the Current Bid, not by your Maximum Bid.&nbsp;As the Current Bid increases, the bid increment increases.</p>
