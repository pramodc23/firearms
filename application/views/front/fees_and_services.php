  <div id="tabs-3" class="col-md-10">
   <div class="buy_cnt_head pull-left">Listing Expiration</div>
    <div class="follow_btnn pull-right"><span class="btn_inner"><a href="#">Follow</a></span></div>
    <div class="article-body">
   <p>Basic Auction Listings in the system can run for up to 90 days if unsold. At that point the listing will expire. The listing expiration date is different from the end date of a listing. The end date of a listing is set based on the listing duration. Items can relist multiple times based on the listing duration before the listing expires. Once a listing expires it will no longer automatically relist. If the item is manually listed again after it expires and the listing uses Optional Services, the fee for those services will be charged again.</p>
<p>All listing types expire. Some listings using selected optional services have different expiration periods than basic listings. Fixed Price Listings expire when they end or sell out. Please see the <a href="#">Fee Schedule</a> for listing expiration periods.</p>
<p>An unsold listing will expire in up to 90 days regardless of how many times it has been relisted / extended, either automatically or manually. Once a listing has expired it will need to be manually listed again. This new listing will have a new expiration date and will be charged for any Optional Services.</p>
<p>Items that sell and are set to relist even if sold will relist with a new expiration date based on the start of the new listing. If Optional Services or Listing Fees are required for the listing those will be charged to the newly listed item. This new item will then run until it expires or sells. The same is true for manual relists of sold or expired items.</p>
<p>Items can be set to relist / extend automatically. This will occur based on the relist / extend option and listing duration selected. However, an item will not relist or extend after the expiration date. Optional services including a special duration or reserve price are limited to a maximum expiration of 4 times the listing duration. The relist / extend selection can be made when the item is initially listed.</p>
<p>Items that are not set to relist / extend automatically can be manually relisted until they expire. Relisting an unsold item before it expires is free.</p>
