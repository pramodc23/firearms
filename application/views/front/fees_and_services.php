  <div id="tabs-4" class="col-md-10">
   <div class="buy_cnt_head pull-left">Listing Expiration</div>
    <div class="follow_btnn pull-right"><span class="btn_inner"><a href="#">Follow</a></span></div>
    <div class="article-body">
  <p>Our Optional Services consist of attention-getting features that help to get more bidders and buyers to look at your item. When a Seller elects to use Optional Services, the seller incurs a small fee at the time of listing as noted in the <a href="#">Fee Schedule</a> below. Optional Service fees are non-refundable once used and must be paid for whether or not the item sells. If you choose to use any of our Optional Services and the item does not sell, the item can be relisted free of charge using the same options until the listing expires.</p>
<p><b>Boldface Title</b><br>
Consider using boldface text to make your item's title stand out from the others in the category listings.</p>
<p><b>Colored Title</b><br>
Upgrade your listing by using color to draw a buyer's attention to your title – you can choose to use red, green, or blue. A colored title is especially effective when combined with the Boldface Title option.</p>
<p><b>Featured Listings</b><br>
Featured listings are given additional exposure in search results and will typically be seen by more bidders, which can draw more bids and command a higher purchase price.</p>
<p><b>Highlight</b><br>
Make your listing stand out from others in the category listings. This option highlights your listing in yellow to make it stand out from the other listings.</p>
<p><b>Reserve Price</b><br>
A Reserve Price is the lowest price at which a seller is willing to sell an item. The Reserve Price is kept secret from the bidders. The item will not sell until a bid meets or exceeds the reserve price.</p>
<p><b>Scheduled Listing</b><br>
A Scheduled Listing is one where you select the start date and time for the item listing instead of launching it immediately. Sellers can use this to enter items before they want them to be displayed so that they have the flexibility to create, enter information and launch the items when it is convenient for them.</p>
<p><b>Showcase Listings</b><br>
Showcase Listings are the most powerful marketing tool our sellers can use. Showcase items are randomly displayed on our Home Page. Our Home Page is the most-visited page on firearms.com. Consequently, Showcase items will be seen by the largest audience of any of our Optional Services.</p>
<p><strong>Special Duration / Short Term Auctions</strong><br>
Special Duration listings include 1-day and 3-day short term auctions.</p>
<p><strong>On-site / Off-site Sponsored Listings</strong><br>
An On-site Sponsored Listing is a listing that appears along the right-hand side of relevant search results pages. These listings are randomly generated on firearms.com.</p>
<p>An Off-site Sponsored Listing appears on other relevant firearms.com partner websites driving traffic back to your listing.</p>
<p>You can learn more about Sponsored Listings <a href="#">here</a>.</p>
<p><b>Subtitle</b><br>
A Subtitle is additional text that is displayed below the Item Title on the search results pages. It is space that can be used to provide buyers with more information regarding your item, and it also improves your item's visibility and increases its searchability.</p>
<p><b>View Counter</b><br>
A view counter keeps track of the number of times an item has been viewed since being listed. Sellers can use counters as a way to gauge the interest bidders have shown in an item. They can also experiment with different item titles and upgrade features to measure the effectiveness of each variation.</p>
