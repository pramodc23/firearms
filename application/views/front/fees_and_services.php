    <div class="article-body">
    <p>We charge no 'insertion' fees for basic listings. Basic listings include no reserve auctions and fixed price listings. A Final Value Fee is assessed only if your item ends with a winning bidder or is purchased using the BuyNow! function. Optional Services can be used to enhance any listing and must be paid for whether or not the item is sold. If you choose to use any of our Optional Services and the item does not sell, it can be automatically relisted free of charge using the same options.</p>
<p>To create a safe buying / selling environment, sellers must verify their identity with us. Sellers will be charged a verification fee of $2.00 to go through a detailed security check. This process is required in order to be able to sell. We reserve the right to deny access to site features if we are unable to verify your identity.</p>
<p>For any item that is won by a bidder or purchased using "BuyNow!", we assess a final value fee based on the selling price of the item. A sold item is one that is bid on and won by a bidder in accordance with our <a href="#">User Agreement</a>. If an item does not sell, no Final Value Fee is assessed. In other words, if you create a basic listing (one that uses no Optional Services) and the item does not sell, you owe us nothing. View a full description of the <a href="#">Final Value Fee</a> here.</p>
<p><b>What's Free</b> – We do not charge any 'insertion' fees for creating a basic listing. Basic listings are completely free unless the item is won by a bidder. (Sold items are assessed a <a href="#finalvaluefee">Final Value Fee</a>). Thumbnails on search results and multiple pictures within the item listing page are free. Sellers can take advantage of our automated picture upload facility to add multiple pictures to their listings; in fact, we strongly recommend doing so.</p>
<p><b>What's Not Free</b> – Sellers are assessed a <a href="#">Final Value Fee</a> when the item sells. When listing an item, the seller decides on an item-by-item basis which of our <a href="#feesummary">Optional Services</a> (if any) to use. Optional Services include things such as highlighting, bold titles, colored titles, view counters, subtitles, showcase, featured and scheduled listings. Auction only optional services include reserve prices and special listing durations. All <a href="#">Optional Services</a> are charged at the time of listing and are non refundable.</p>
