   <div class="buy_cnt_head pull-left">Final Value Fee</div>
    <div class="follow_btnn pull-right"><span class="btn_inner"><a href="#">Follow</a></span></div>
    <div class="article-body">
 <p>A Final Value Fee is assessed if your item is sold. The Final Value Fee is assessed when the listing closes and is based on the price that the item is sold for. If the item is not sold there is no Final Value Fee. You can use our <a href="#" target="_blank" rel="noopener">Final Value Fee Calculator</a> to find out the final value fee associated with the selling price of an item.</p>
<p>Here is how you calculate your Final Value Fee:</p>
<ol>
<li>Take the first $250 of your final value and multiply it by 6%. If your item sold for $250 or less this is your Final Value Fee.</li>
<li>If your final value was more than $250, take the additional amount over $250 and multiply it by 3.5%.</li>
<li>Add up these amounts and that is your Final Value Fee.</li>
</ol>
<p>For example, a $1500 item would cost:</p>
<ol>
<li>$250 times 6% = $15.00</li>
<li>$1250 times 3.5% = $43.75</li>
<li>Total = $58.75</li>
</ol>
<p>A $500 item would cost:</p>
<ol>
<li>$250 times 6% = $15.00</li>
<li>$250 times 3.5% = $8.75</li>
<li>Total = $23.75</li>
</ol>
