    <div class="follow_btnn pull-right"><span class="btn_inner"><a href="#">Follow</a></span></div>
    <div class="article-body">
  <p>Each seller must place a payment method (bank account or credit card) on file with us to be able to list items for sale. To place a bank account or credit card on file, go to My firearms &gt; My Account &gt; Billing Info and select the <a href="#">add bank account</a> or <a href="#">add credit card</a> link.</p>
<p>Any fees you incur will be accumulated throughout a calendar month. Around the 2nd of the month, the seller will receive a statement showing fees for the prior month. The balance due on the statement will be billed to the payment method on file. For example, if you sell an item in January, your statement will be available on or after the 2nd of February; payment will be due the 20th of February.</p>
<p>If your payment method fails to authorize, you will not be able to list additional items until you update your payment method on file. If your payment method fails to authorize, your account must be paid promptly; past due users will be suspended from using the site until the balance has been paid in full.</p>
<p><b>Secondary Payment Method:</b> You may specify a secondary payment method for us to use to help keep your account current. If for some reason the Primary Payment Method fails to process successfully, we will attempt to charge your Secondary Payment Method. After placing an additional payment method on file, you would then specify Primary and Secondary Payment methods in the Automatic Payment Methods section of My firearms &gt; My Account &gt; <a href="#">Billing Info</a>.</p>
<p><b>Manual Payment:</b> If you wish to make a payment using a different payment method than the one on file, you can go to the My firearms &gt; My Account &gt; <a href="#">Billing Info</a> section of our web site and click Make a Payment. The payment must be received before the 20th of the month, or your payment method on file will be charged for your balance due.</p>
<p><b>Payments via Check or Money Order:</b> The <a href="#">mailing address</a> is located on the payment stub on your statement. Please be sure to write your account number on your check. Your payment must be received by the 20th of the month or your payment method on file will be charged for your Balance Due.</p>
<p>See this <a href="#">FAQ for help reading your billing statement</a>.</p>
